                              <header class="heading">
                                    <h2>How We Do It</h2>
                              </header>
                              <div class="wrapper">

                                    <!-- mobilization -->
                                    <div class="process mobilization">
                                          <a class="modal-trigger" href="#modal1">
                                                <picture>
                                                      <source srcset="./images/index/process/mobilization.png" type="image/png">
                                                      <img src="./images/index/process/mobilization.png" alt="" width="235" height="240"/>
                                                </picture>
                                          </a>
                                          <div id="modal1" class="modal">
                                                <div class="modal-content">
                                                      <strong>EvenCargo</strong>, in partnership with civil society organizations works
                                                      to
                                                      <strong>mobilize young women</strong> and works to understand their experience and
                                                      their aspirations. Together, with the
                                                      <strong>young women</strong>, we assist them in building career trajectory plans
                                                      that include using their work at
                                                      <strong>EvenCargo</strong> as an “earn and learn” opportunity.
                                                      <br>
                                                      <br> Whether it be examining ways for the young woman to enroll in open schooling
                                                      or take additional training towards achieving her dreams, EC empowers her
                                                      to realise her dreams and provides a vehicle and opportunity to fulfill them.
                                                </div>
                                          </div>
                                    </div>

                                    <!-- training -->
                                    <div class="process training">
                                          <a class="modal-trigger" href="#modal2">
                                                <picture>
                                                      <source srcset="./images/index/process/training.png" type="image/png">
                                                      <img src="./images/index/process/training.png" alt="" width="235" height="240"/>
                                                </picture>
                                          </a>
                                          <div id="modal2" class="modal">
                                                <div class="modal-content">
                                                      <span>Currently the EC training includes four modules:</span>
                                                      <br>
                                                      <br>
                                                      <ul>
                                                            <li>Two-wheeler driving with Honda Traffic Training Park.</li>
                                                            <li>Self-Defense with the Delhi Police.</li>
                                                            <li>Introduction to the Logistics Sector with Amazon.</li>
                                                            <li>Employability Skills (Communication, Conflict / Stress / Time Management).</li>
                                                            <li style="list-style-type: disc;">As it currently stands, the training process takes about 1 week. However,
                                                                  upon a review of our progress last year, we are in the process
                                                                  of expanding this training to be between 3 and 4 weeks.
                                                            </li>
                                                </div>
                                          </div>
                                    </div>

                                    <!-- employment -->
                                    <div class="process employment">
                                          <a class="modal-trigger" href="#modal3">
                                                <picture>
                                                      <source srcset="./images/index/process/employment.png" type="image/png">
                                                      <img src="./images/index/process/employment.png" alt="" width="235" height="240"/>
                                                </picture>
                                          </a>
                                          <div id="modal3" class="modal">
                                                <div class="modal-content">
                                                      Young women are then sent to E-Commerce Companies to deliver packages within the predetermined vicinities. They are held
                                                      to the same standards as the other delivery personnel, which many of our
                                                      young women have pointed out has helped them to understand the logistics
                                                      sector quickly.
                                                      <br>
                                                      <br> Further, this has helped absorb the women more quickly into the ecosystem.
                                                      While there are a few tweaks in the traditional delivery model, in order
                                                      to help ensure the safety of the women riders as well as accommodate the
                                                      additional responsibilities that they juggle.
                                                </div>
                                          </div>
                                    </div>

                                    <!-- retention -->
                                    <div class="process retention">
                                          <a class="modal-trigger" href="#modal4">
                                                <picture>
                                                      <source srcset="./images/index/process/retention-support.png" type="image/png">
                                                      <img src="./images/index/process/retention-support.png" alt="" width="235" height="240"/>
                                                </picture>
                                          </a>
                                          <div id="modal4" class="modal">
                                                <div class="modal-content">
                                                      <span>Our engagement is targeted to happen at two levels:</span>
                                                      <br>
                                                      <br>
                                                      <ul>
                                                            <li>
                                                                  <span>The working women:</span>
                                                                  <br> One of the largest problems faced by employers in today’s job
                                                                  market is that of high attrition rate of employees. Therefore
                                                                  to address this issue,
                                                                  <strong>EvenCargo</strong> conducts specific programmes to
                                                                  <strong>support women</strong> through the challenges of a career and
                                                                  ensure retention in their jobs. The programmes also include regular
                                                                  meetings, professional development sessions, sessions on navigating
                                                                  the balance between work and professional life, stress and anger
                                                                  management courses.
                                                                  <br> Lastly, this programming also equips them to handle pressure
                                                                  societal pressure. Parental and familial counseling are also
                                                                  the part of session, to help them understand the need for gender
                                                                  equity at home and workplaces.</li>
                                                            <li>
                                                                  <span>The e-commerce companies:</span>
                                                                  <br> The e-commerce companies are eager to diversify their delivery
                                                                  force through the inclusion of
                                                                  <strong>female rider partners</strong>. They have been relying on
                                                                  <strong>EvenCargo</strong> to provide regular inputs into their systems
                                                                  and are open to understand how they can support more women rider
                                                                  partners.
                                                                  <br> To date, We have advocated and helped change the working hours
                                                                  for
                                                                  <strong>female rider partners</strong> from
                                                                  <span>7AM</span> to
                                                                  <span>3PM</span> to
                                                                  <span>9AM</span> to </span>5PM</span> to respect the household responsibilities
                                                                  that the women finish and then come to work. We are currently
                                                                  in the process of planning a series of gender-sensitization workshops
                                                                  with corporates to further cement the foundation of employing
                                                                  women in a female friendly work space.</li>
                                                </div>
                                          </div>
                                    </div>
                              </div>
